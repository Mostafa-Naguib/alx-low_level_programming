#include "3-calc.h"


/**
 * op_add - adding two numbers
 *
 * Description: adding two numbers
 * @a: The first number
 * @b: The Second number
 *
 * Return: The result
 */

int op_add(int a, int b) { return (a + b); }


/**
 * op_sub - subtract two numbers
 *
 * Description: subtract two numbers
 * @a: The first number
 * @b: The Second number
 *
 * Return: The result
 */

int op_sub(int a, int b) { return (a - b); }


/**
 * op_mul - multibly two numbers
 *
 * Description: multibly two numbers
 * @a: The first number
 * @b: The Second number
 *
 * Return: The result
 */

int op_mul(int a, int b) { return (a * b); }


/**
 * op_div - divide two numbers
 *
 * Description: dividing two numbers
 * @a: The first number
 * @b: The Second number
 *
 * Return: The result
 */

int op_div(int a, int b) { return (a / b); }


/**
 * op_mod - mod two numbers
 *
 * Description: mod two numbers
 * @a: The first number
 * @b: The Second number
 *
 * Return: The result
 */

int op_mod(int a, int b) { return (a % b); }

