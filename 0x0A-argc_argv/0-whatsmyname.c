#include <stdio.h>
#include <stdlib.h>

/**
 * main - prints its name, followed by a new line.
 *
 * Description: prints its name, followed by a new line.
 * @argc: The number of argv
 * @argv: The array
 *
 * return: Always 0.
 */


int main(int argc, char *argv[])
{
	printf("%s\n", argv[argc - argc]);

	return (0);
}
